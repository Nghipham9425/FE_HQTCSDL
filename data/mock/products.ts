export function getProducts(filters?: {
  category?: Product["category"];
  onSale?: boolean;
  inStock?: boolean;
  newArrival?: boolean;
  bestSeller?: boolean;
  search?: string;
}): Product[] {
  let result = [...products];
  if (filters?.category) result = result.filter((p) => p.category === filters.category);
  if (filters?.onSale) result = result.filter((p) => p.isOnSale);
  if (filters?.inStock) result = result.filter((p) => p.inStock);
  if (filters?.newArrival) result = result.filter((p) => p.isNewArrival);
  if (filters?.bestSeller) result = result.filter((p) => p.isBestSeller);
  if (filters?.search) {
    const q = filters.search.toLowerCase();
    result = result.filter(
      (p) =>
        p.name.toLowerCase().includes(q) ||
        p.brand.toLowerCase().includes(q) ||
        p.category.includes(q)
    );
  }
  return result;
}

export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}
